@dataclass
class ReconcileResult:
    """Outcome of one reconciliation attempt on one block."""
    method: str
    n: int
    leaked_bits: int
    syndrome_rate: float
    claimed_success: bool       # decoder itself thinks it converged/found e
    actually_correct: bool      # ground truth: corrected key == Alice's key
    time_s: float
    work_units: int             # BP iterations or GRAND guesses

    @property
    def undetected_error(self) -> bool:
        """Decoder claimed success but the key is wrong (would need hash check)."""
        return self.claimed_success and not self.actually_correct

    @property
    def net_key_fraction(self) -> float:
        """Fraction of the block surviving as corrected key: (n - m)/n if correct, else 0."""
        if not self.actually_correct:
            return 0.0
        return max(0.0, (self.n - self.leaked_bits) / self.n)


def result_row(r: ReconcileResult, qber_label: str, p_true: float, p_est: float, trial: int) -> dict:
    """Flatten a ReconcileResult into a DataFrame-friendly dict."""
    return {
        "method": r.method,
        "n": r.n,
        "qber_label": qber_label,
        "p_true": p_true,
        "p_est": p_est,
        "trial": trial,
        "leaked_bits": r.leaked_bits,
        "syndrome_rate": r.syndrome_rate,
        "claimed_success": r.claimed_success,
        "correct": r.actually_correct,
        "undetected_error": r.undetected_error,
        "net_key_fraction": r.net_key_fraction,
        "time_s": r.time_s,
        "work_units": r.work_units,
    }
